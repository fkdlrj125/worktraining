package com.spring.recruit.vo;

public class CertVo {
	String certSeq;
	String recSeq;
	String certQualifi;
	String certAcqu;
	String certOrganize;
	
	public String getCertSeq() {
		return certSeq;
	}
	public void setCertSeq(String certSeq) {
		this.certSeq = certSeq;
	}
	public String getRecSeq() {
		return recSeq;
	}
	public void setRecSeq(String recSeq) {
		this.recSeq = recSeq;
	}
	public String getCertQualifi() {
		return certQualifi;
	}
	public void setCertQualifi(String certQualifi) {
		this.certQualifi = certQualifi;
	}
	public String getCertAcqu() {
		return certAcqu;
	}
	public void setCertAcqu(String certAcqu) {
		this.certAcqu = certAcqu;
	}
	public String getCertOrganize() {
		return certOrganize;
	}
	public void setCertOrganize(String certOrganize) {
		this.certOrganize = certOrganize;
	}
	
	
}
