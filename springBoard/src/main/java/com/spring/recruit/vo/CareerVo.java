package com.spring.recruit.vo;

public class CareerVo {
	String carSeq;
	String recSeq;
	String carCompany;
	String carLoc;
	String carStart;
	String carEnd;
	String carTask;
	String carSalary;
	
	public String getCarSeq() {
		return carSeq;
	}
	public void setCarSeq(String carSeq) {
		this.carSeq = carSeq;
	}
	public String getRecSeq() {
		return recSeq;
	}
	public void setRecSeq(String recSeq) {
		this.recSeq = recSeq;
	}
	public String getCarCompany() {
		return carCompany;
	}
	public void setCarCompany(String carCompany) {
		this.carCompany = carCompany;
	}
	public String getCarLoc() {
		return carLoc;
	}
	public void setCarLoc(String carLoc) {
		this.carLoc = carLoc;
	}
	public String getCarStart() {
		return carStart;
	}
	public void setCarStart(String carStart) {
		this.carStart = carStart;
	}
	public String getCarEnd() {
		return carEnd;
	}
	public void setCarEnd(String carEnd) {
		this.carEnd = carEnd;
	}
	public String getCarTask() {
		return carTask;
	}
	public void setCarTask(String carTask) {
		this.carTask = carTask;
	}
	public String getCarSalary() {
		return carSalary;
	}
	public void setCarSalary(String carSalary) {
		this.carSalary = carSalary;
	}
}
