package com.spring.recruit.vo;

public class RecruitVo {
	String recSeq;
	String recName;
	String recBirth;
	String recGender;
	String recPhone;
	String recEmail;
	String recAddr;
	String recLoc;
	String recWt;
	String recSubmit;
	
	public String getRecSeq() {
		return recSeq;
	}
	public void setRecSeq(String recSeq) {
		this.recSeq = recSeq;
	}
	public String getRecName() {
		return recName;
	}
	public void setRecName(String recName) {
		this.recName = recName;
	}
	public String getRecBirth() {
		return recBirth;
	}
	public void setRecBirth(String recBirth) {
		this.recBirth = recBirth;
	}
	public String getRecGender() {
		return recGender;
	}
	public void setRecGender(String recGender) {
		this.recGender = recGender;
	}
	public String getRecPhone() {
		return recPhone;
	}
	public void setRecPhone(String recPhone) {
		this.recPhone = recPhone;
	}
	public String getRecEmail() {
		return recEmail;
	}
	public void setRecEmail(String recEmail) {
		this.recEmail = recEmail;
	}
	public String getRecAddr() {
		return recAddr;
	}
	public void setRecAddr(String recAddr) {
		this.recAddr = recAddr;
	}
	public String getRecLoc() {
		return recLoc;
	}
	public void setRecLoc(String recLoc) {
		this.recLoc = recLoc;
	}
	public String getRecWt() {
		return recWt;
	}
	public void setRecWt(String recWt) {
		this.recWt = recWt;
	}
	public String getRecSubmit() {
		return recSubmit;
	}
	public void setRecSubmit(String recSubmit) {
		this.recSubmit = recSubmit;
	}
}
